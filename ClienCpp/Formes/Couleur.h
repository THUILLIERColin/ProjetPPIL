/**
 * @brief Cette classe enum permet de définir une couleur a partir de valeurs hexadécimales
 * Elle est faite pour simplifier l'utilisation des couleurs dans le programme
 */
enum class Couleur {
    ROUGE = 0xFF0000,
    VERT = 0x00FF00,
    BLEU = 0x0000FF,
    BLANC = 0xFFFFFF,
    NOIR = 0x000000,
    JAUNE = 0xFFFF00,
    CYAN = 0x00FFFF,
    MAGENTA = 0xFF00FF,
    GRIS = 0x808080
};